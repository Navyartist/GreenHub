pokemon = [
    ["one", "pikachu"],
    ["two", "charmander"],
    ["three", "bulbasaur"]
]
# 행렬 구조 or 2차원 배열
# 엑셀의 셀과 유사한 개념
print(pokemon[0][1])  # Output: pikachu
print(pokemon[1][1])  # Output: charmander
print(pokemon[1][0])  # Output: two
# 객체(딕셔너리): 속성(property)
# 배열(리스트): 요소(element) 또는 원소(item)

# 품질이 좋은 코드일수록 알아보기가 힘들다
# coding pattern = design pattern
# 디자인 패턴: 자주 발생하는 문제를 해결하기 위한 재사용 가능한 솔루션
# syntax pattern(암호 표기법): 모든 개발자가 동일한 것